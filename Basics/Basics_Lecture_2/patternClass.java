package Basics.Basics_Lecture_2;

class patternClass {
    public static void main(String[] args) {
    // Example usage of a pattern class
    int rows = 5;

    for (int i = 1; i <= rows; i++) {
        for (int j = 1; j <= i; j++) {
            System.out.print("* ");
        }
        System.out.println();
    }
}
}






